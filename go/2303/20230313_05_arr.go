package main

import "fmt"

func main() {
	fmt.Println("------------------------------")
	// var 변수명 [요소개수]타입
	var t [5]float64 = [5]float64{24.0, 25.9, 27.8, 26.9, 26.2}
	for i := 0; i < 5; i++ {
		fmt.Println(t[i])
	}
	fmt.Println("------------------------------")
	var nums [5]int
	for i := 0; i < 5; i++ {
		fmt.Println(nums[i])
	}
	fmt.Println("------------------------------")
	days := [3]string{"monday", "tuesday", "wednesday"}
	for i := 0; i < 3; i++ {
		fmt.Println(days[i])
	}
	fmt.Println("------------------------------")
	var temps [5]float64 = [5]float64{24.3, 26.7}
	for i := 0; i < 5; i++ {
		fmt.Println(temps[i])
	}
	fmt.Println("------------------------------")
	var s = [5]int{1: 10, 3: 30}
	for i := 0; i < 5; i++ {
		fmt.Println(s[i])
	}
	fmt.Println("------------------------------")
	x := [...]int{10, 20, 30}
	for i := 0; i < 3; i++ {
		fmt.Println(x[i])
	}
	fmt.Println("------------------------------")
	const X1 int = 3
	// x2 := 5
	// a1 := [x2]int{1, 2, 3} // error
	a2 := [X1]int{1, 2, 3} // OK
	for i := 0; i < X1; i++ {
		fmt.Println(a2[i])
	}
	fmt.Println("------------------------------")
	var a3 [3]int // OK
	for i := 0; i < 3; i++ {
		fmt.Println(a3[i])
	}
	fmt.Println("------------------------------")
}

