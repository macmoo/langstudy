package main

import "fmt"

func main() {
	fmt.Println("---------------------------------")
	s1 := make([]int, 3, 5)
	s2 := append(s1, 4, 5)
	fmt.Println("s1:", s1, len(s1), cap(s1))
	fmt.Println("s2:", s2, len(s2), cap(s2))
	fmt.Println("---------------------------------")
	s1[1] = 100
	fmt.Println("s1:", s1, len(s1), cap(s1))
	fmt.Println("s2:", s2, len(s2), cap(s2))
	fmt.Println("---------------------------------")
	s1 = append(s1, 500)
	fmt.Println("s1:", s1, len(s1), cap(s1))
	fmt.Println("s2:", s2, len(s2), cap(s2))
	fmt.Println("---------------------------------")
	fmt.Println("---------------------------------")
	s3 := []int{1, 2, 3}
	s4 := append(s3, 4, 5)
	fmt.Println("s3:", s3, len(s3), cap(s3))
	fmt.Println("s4:", s4, len(s4), cap(s4))
	fmt.Println("---------------------------------")
	s3[1] = 100
	fmt.Println("s3:", s3, len(s3), cap(s3))
	fmt.Println("s4:", s4, len(s4), cap(s4))
	fmt.Println("---------------------------------")
	s3 = append(s3, 500)
	fmt.Println("s3:", s3, len(s3), cap(s3))
	fmt.Println("s4:", s4, len(s4), cap(s4))
	fmt.Println("---------------------------------")
}

